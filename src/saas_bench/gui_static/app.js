async function callTool(name, args, { reload = true } = {}) {
  const result = await api(`/api/tool/${name}`, {
    method: "POST",
    body: JSON.stringify({ args }),
  });
  toast(result.message || `${name} applied`);
  if (reload) await loadState();
  return result;
}

function valuesForPlans(prefix) {
  return Object.fromEntries(plans.map((p) => [p, Number($(`${prefix}${p}`).value)]));
}

async function applyCore() {
  const updates = {
    prices: valuesForPlans("price"),
    tiers: valuesForPlans("tier"),
    quotas: valuesForPlans("quota"),
    spend: {
      operations: Number($("opsSpend").value),
      development: Number($("devSpend").value),
    },
    capacity: { tier: Number($("capacityTier").value) },
    adsStrength: { global_strength: Number($("adsStrength").value) },
    promotion: { global_promotion: Number($("promoGlobal").value) },
    leadPromotion: { global_promotion: Number($("leadPromoGlobal").value) },
  };

  await callTool("set_prices", updates.prices, { reload: false });
  await callTool("set_model_tiers", updates.tiers, { reload: false });
  await callTool("set_usage_quotas", updates.quotas, { reload: false });
  await callTool("set_daily_spend", updates.spend, { reload: false });
  await callTool("set_capacity_tier", updates.capacity, { reload: false });
  await callTool("set_ads_strength", updates.adsStrength, { reload: false });
  await callTool("set_promotion", updates.promotion, { reload: false });
  await callTool("set_lead_promotion", updates.leadPromotion, { reload: false });
  await loadState();
  toast("Core settings applied");
}

async function applyAds() {
  const groups = knownGroups();
  const targeted_spend = {};
  for (const ch of channels) {
    const row = {};
    for (const g of groups) {
      const v = Number($(`ad_${ch}_${g}`).value || 0);
      if (v > 0) row[g] = v;
    }
    if (Object.keys(row).length) targeted_spend[ch] = row;
  }
  await callTool("set_targeted_ad_spend", { targeted_spend });
}

async function applyTargetedDev() {
  const targeted_spend = {};
  for (const g of knownGroups()) {
    const v = Number($(`dev_${g}`).value || 0);
    if (v > 0) targeted_spend[g] = v;
  }
  await callTool("set_targeted_dev_spend", { targeted_spend });
}

async function applyTargetedOps() {
  const by_group = {};
  for (const g of knownGroups()) {
    const v = Number($(`ops_${g}`).value || 0);
    if (v > 0) by_group[g] = v;
  }
  await callTool("set_targeted_ops_spend", { by_group });
}

function forecasts() {
  const keys = ["cash_1wk", "cash_4wk", "cash_12wk", "cash_26wk"];
  return Object.fromEntries(keys.map((k) => [k, {
    point: Number($(`${k}_point`).value),
    lower: Number($(`${k}_lower`).value),
    upper: Number($(`${k}_upper`).value),
  }]));
}

function setupEvents() {
  document.querySelectorAll(".tab").forEach((btn) => {
    btn.addEventListener("click", () => {
      document.querySelectorAll(".tab").forEach((b) => b.classList.remove("active"));
      document.querySelectorAll(".tab-panel").forEach((p) => p.classList.remove("active"));
      btn.classList.add("active");
      $(btn.dataset.tab).classList.add("active");
    });
  });
  $("refreshBtn").addEventListener("click", loadState);
  $("applyCoreBtn").addEventListener("click", () => wrap(applyCore));
  $("applyAdsBtn").addEventListener("click", () => wrap(applyAds));
  $("applyDevBtn").addEventListener("click", () => wrap(applyTargetedDev));
  $("applyOpsBtn").addEventListener("click", () => wrap(applyTargetedOps));
  $("researchMarketBtn").addEventListener("click", () => wrap(() => callTool("research_market", {})));
  $("researchGroupBtn").addEventListener("click", () => wrap(() => callTool("research_group", {
    group_id: $("researchGroupId").value.trim(),
    target_level: Number($("researchLevel").value),
  })));
  $("startRndBtn").addEventListener("click", () => wrap(() => callTool("start_research_project", {
    tier: Number($("rndTier").value),
  })));
  $("sendDealBtn").addEventListener("click", () => wrap(() => callTool("send_enterprise_deal", {
    deals: [[Number($("dealCustomerId").value), [[$("dealPlan").value, Number($("dealPrice").value)]]]],
  })));
  $("rejectDealBtn").addEventListener("click", () => wrap(() => callTool("reject_enterprise_deal", {
    deals: [{ customer_id: Number($("dealCustomerId").value) }],
  })));
  $("advanceBtn").addEventListener("click", () => wrap(async () => {
    setBusy(true);
    try {
      const data = await api("/api/advance-week", {
        method: "POST",
        body: JSON.stringify({ rationale: $("rationale").value, predictions: forecasts() }),
      });
      state = data.state;
      render();
      toast("Week simulated");
    } finally {
      setBusy(false);
    }
  }));
  $("resetBtn").addEventListener("click", () => wrap(async () => {
    if (!confirm("Reset the current game and start from day 0?")) return;
    state = await api("/api/reset", {
      method: "POST",
      body: JSON.stringify({ seed: 42, days: 500, initial_cash: 1000000 }),
    });
    render();
    toast("Game reset");
  }));
}

async function wrap(fn) {
  try {
    await fn();
  } catch (err) {
    toast(err.message || String(err));
  }
}

setupEvents();
