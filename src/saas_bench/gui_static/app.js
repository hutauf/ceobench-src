function money(v) {
  if (v === null || v === undefined || Number.isNaN(Number(v))) return "-";
  return `$${Number(v).toLocaleString(undefined, { maximumFractionDigits: 0 })}`;
}

function num(v) {
  if (v === null || v === undefined || Number.isNaN(Number(v))) return "-";
  return Number(v).toLocaleString(undefined, { maximumFractionDigits: 0 });
}

function pct(v) {
  if (v === null || v === undefined || Number.isNaN(Number(v))) return "-";
  return `${(Number(v) * 100).toFixed(2)}%`;
}

function escapeHtml(value) {
  return String(value)
    .replaceAll("&", "&amp;")
    .replaceAll("<", "&lt;")
    .replaceAll(">", "&gt;")
    .replaceAll('"', "&quot;")
    .replaceAll("'", "&#39;");
}

function tip(key) {
  const text = helpText[key];
  if (!text) return "";
  const safe = escapeHtml(text);
  return `<span class="info-tip" tabindex="0" role="tooltip" title="${safe}" aria-label="${safe}" data-tip="${safe}">?</span>`;
}

function labelText(text, key) {
  return `<span class="label-text">${escapeHtml(text)}${tip(key)}</span>`;
}

function controlLabel(text, key, inputHtml) {
  return `<label>${labelText(text, key)}${inputHtml}</label>`;
}

async function api(path, options = {}) {
  const res = await fetch(path, {
    headers: { "Content-Type": "application/json" },
    ...options,
  });
  const data = await res.json();
  if (!res.ok || data.success === false) {
    throw new Error(data.detail || data.message || data.error || "Request failed");
  }
  return data;
}

function toast(message) {
  const el = $("toast");
  el.textContent = message;
  el.classList.add("show");
  setTimeout(() => el.classList.remove("show"), 3600);
}

function setBusy(on) {
  $("busy").classList.toggle("hidden", !on);
}

